// regen-decisions-index.mjs — deterministic generator for the "Standing Decisions" block.
//
//   node "${CLAUDE_PLUGIN_ROOT}/skills/_shared/regen-decisions-index.mjs" [--check] [root]
//
// Reads <root>/.rpiv/decisions/*.md, keeps the entries with `status: accepted`, and renders a
// one-line-per-decision index bounded by sentinel comments into <root>/CLAUDE.md (the always-
// loaded project overview — NOT the per-directory CLAUDE.md files).
//   default : rewrite the block — insert at end if absent, replace in place if present, and
//             remove the block entirely when there are no accepted decisions.
//   --check : exit 1 if the block is missing/stale relative to the decision files; writes nothing.
//             This is the gate the artifact lint (validate-artifact.mjs) reuses so a `ready` flip
//             fails when the ledger and CLAUDE.md have drifted.
//
// The injected one-liner per decision is the `rule:` frontmatter field — the decision body is
// NEVER parsed, so the generator stays deterministic. `status: accepted` is injected; `proposed`
// and `retired` are kept on disk for history but drop out of the block.
//
// Importable: readDecisions / renderBlock / extractBlock / checkBlock / writeBlock are exported
// for validate-artifact.mjs. Running as a script performs the CLI behavior above.

import { execFileSync } from "node:child_process";
import { existsSync, readdirSync, readFileSync, realpathSync, writeFileSync } from "node:fs";
import { join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const BEGIN =
	"<!-- BEGIN standing-decisions (generated by regen-decisions-index.mjs — do not edit inside) -->";
const END = "<!-- END standing-decisions -->";

const toPosix = (p) => p.replaceAll("\\", "/");

/** Resolve the repo root: explicit arg, else git toplevel, else cwd. */
export function repoRoot(argRoot) {
	if (argRoot) return toPosix(argRoot);
	try {
		const top = execFileSync("git", ["rev-parse", "--show-toplevel"], {
			encoding: "utf-8",
			stdio: ["ignore", "pipe", "ignore"],
		}).trim();
		if (top) return toPosix(top);
	} catch {
		/* not a git repo — fall through */
	}
	return toPosix(process.cwd());
}

/** Minimal single-line-value frontmatter parser (deterministic; no multi-line values). */
export function parseFrontmatter(text) {
	if (!text.startsWith("---")) return {};
	const end = text.indexOf("\n---", 3);
	if (end === -1) return {};
	const fm = {};
	for (const line of text.slice(3, end).split("\n")) {
		const m = line.match(/^([A-Za-z0-9_-]+):\s*(.*)$/);
		if (!m) continue;
		let v = m[2].trim();
		if ((v.startsWith('"') && v.endsWith('"')) || (v.startsWith("'") && v.endsWith("'"))) {
			v = v.slice(1, -1);
		}
		fm[m[1]] = v;
	}
	return fm;
}

/** Split an artifact into { fm, body }: parsed frontmatter map + the text after the closing `---`. */
export function splitFrontmatter(text) {
	if (!text.startsWith("---")) return { fm: {}, body: text };
	const end = text.indexOf("\n---", 3);
	if (end === -1) return { fm: {}, body: text };
	const afterClose = text.indexOf("\n", end + 4);
	const body = afterClose === -1 ? "" : text.slice(afterClose + 1);
	return { fm: parseFrontmatter(text), body };
}

/** Read every decision file's frontmatter. Files whose name starts with `_` are skipped. */
export function readDecisions(root) {
	const dir = join(root, ".rpiv", "decisions");
	if (!existsSync(dir)) return [];
	return readdirSync(dir)
		.filter((f) => f.endsWith(".md") && !f.startsWith("_"))
		.map((file) => {
			const fm = parseFrontmatter(readFileSync(join(dir, file), "utf-8"));
			return { file, key: fm.key ?? file.replace(/\.md$/, ""), status: fm.status ?? "", rule: fm.rule ?? "" };
		});
}

/** Render the full sentinel-bounded region, or "" when there are no accepted decisions. */
export function renderBlock(decisions) {
	const accepted = decisions
		.filter((d) => d.status === "accepted")
		.sort((a, b) => a.key.localeCompare(b.key));
	if (accepted.length === 0) return "";
	const lines = accepted.map((d) => `- **${d.key}** — ${d.rule} [.rpiv/decisions/${d.file}]`);
	return [
		BEGIN,
		"## Standing Decisions",
		"",
		"Binding decisions that outlive their originating feature. Full rationale in `.rpiv/decisions/<key>.md`.",
		"",
		...lines,
		END,
	].join("\n");
}

/** Extract the current region from CLAUDE.md text, or "" if the sentinels are absent. */
export function extractBlock(claudeMd) {
	const re = new RegExp(`${escapeRe(BEGIN)}[\\s\\S]*?${escapeRe(END)}`);
	return claudeMd.match(re)?.[0] ?? "";
}

function escapeRe(s) {
	return s.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

/** Splice `desired` into CLAUDE.md text: replace the region if present, append if not, remove if empty. */
function applyBlock(claudeMd, desired) {
	const re = new RegExp(`${escapeRe(BEGIN)}[\\s\\S]*?${escapeRe(END)}`);
	if (re.test(claudeMd)) {
		if (desired === "") {
			// Remove the region and collapse the blank lines it leaves behind.
			return claudeMd.replace(new RegExp(`\\n*${escapeRe(BEGIN)}[\\s\\S]*?${escapeRe(END)}\\n*`), "\n");
		}
		return claudeMd.replace(re, desired);
	}
	if (desired === "") return claudeMd;
	const trimmed = claudeMd.replace(/\s+$/, "");
	return `${trimmed}\n\n${desired}\n`;
}

/** True when CLAUDE.md's block already matches what the decision files would render. */
export function checkBlock(root) {
	const desired = renderBlock(readDecisions(root));
	const claudePath = join(root, "CLAUDE.md");
	const claudeMd = existsSync(claudePath) ? readFileSync(claudePath, "utf-8") : "";
	return extractBlock(claudeMd) === desired;
}

/** Write the block into CLAUDE.md. Returns true when a change was made. */
export function writeBlock(root) {
	const desired = renderBlock(readDecisions(root));
	const claudePath = join(root, "CLAUDE.md");
	const claudeMd = existsSync(claudePath) ? readFileSync(claudePath, "utf-8") : "";
	const next = applyBlock(claudeMd, desired);
	if (next !== claudeMd) {
		writeFileSync(claudePath, next);
		return true;
	}
	return false;
}

// ---- CLI ---------------------------------------------------------------------------------------
const isMain = (() => {
	try {
		return realpathSync(fileURLToPath(import.meta.url)) === realpathSync(resolve(process.argv[1] ?? ""));
	} catch {
		return false;
	}
})();
if (isMain) {
	const args = process.argv.slice(2);
	const check = args.includes("--check");
	const root = repoRoot(args.find((a) => !a.startsWith("--")));
	if (check) {
		if (checkBlock(root)) {
			process.stdout.write("decisions index: up to date\n");
			process.exit(0);
		}
		process.stderr.write(
			`decisions index: STALE — CLAUDE.md 'Standing Decisions' block does not match .rpiv/decisions/*.md\n` +
				`  fix: node "${toPosix(fileURLToPath(import.meta.url))}" "${root}"\n`,
		);
		process.exit(1);
	}
	const changed = writeBlock(root);
	process.stdout.write(changed ? "decisions index: updated CLAUDE.md\n" : "decisions index: no change\n");
	process.exit(0);
}
